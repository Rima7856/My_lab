#     Правильный падеж - 'час'
#   Иначе если  в 12часом формате меньше 5 и больше или равно 2
#     Правильный падеж - 'часа'
#   Иначе
#     Правильный падеж - 'часов'



def hours_case(h):
    h_12 = h % 12
    if h_12 == 1:
        padej = 'час'
    elif 2 <= h_12 < 5:
        padej = 'часа'
    else:
        padej = 'часов'
def minuts_case(m):
    rovno = m == 0
    if 5 <= m <= 20:
        padej = 'минут'
    else:
        last_cou = m % 10
        if last_cou == 1:
            padej = 'минута'
        elif 2 <= last_cou < 5:
            padej = 'минуты'
        else:
            padej = 'минут'
    return rovno, padej
