# Вычисление площади и периметра прямоугольника

# Ввод данных от пользователя
print("Введите длину прямоугольника: ")
a = float(input())  # Функция input() читает строку, float() преобразует ее в число

print("Введите ширину прямоугольника: ")
b = float(input())

# Вычисление площади и периметра
area = a * b
perimeter = 2 * (a + b)

# Вывод результатов
print("\n--- Результаты вычислений ---")
print(f"Длина: {a}")
print(f"Ширина: {b}")
print(f"Площадь: {area}")
print(f"Периметр: {perimeter}")