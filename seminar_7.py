# Задача 2
#
# Сгенерировать список из 7 случайных целых чисел в диапазоне от 50 до 500
# Вывести список
# Определить длину списка
#
# Для каждой позиции списка от первой до предпоследней:
#   Максимальный индекс равен текущей позиции
#   Перебрать все элементы справа от текущей позиции
#       Если найден элемент больше текущего максимального:
#           Максимальный индекс равен индексу этого числа
#
#   Если максимальный элемент не стоит на текущей позиции, поменять его местами с элементом на текущей позиции
#
#   Вывести список
#
# Вывести итоговый список


import random

def selection_sort_desc(a):
    n = len(a)
    comparisons = 0
    for i in range(n - 1):
        max_index = i
        for j in range(i + 1, n):
            comparisons += 1
            if a[j] > a[max_index]:
                max_index = j
        if max_index != i:
            a[i], a[max_index] = a[max_index], a[i]
            print(a)

expenses = [random.randint(50, 500) for _ in range(7)]
print("Сгенерирован список расходов:", expenses)
selection_sort_desc(expenses)
print(expenses, 'конечный список')


