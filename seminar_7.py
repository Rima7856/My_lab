# Для каждой позиции от первой до предпоследней:
#   Флаг отсутствия обменов равен True
#   Перебрать элементы от начала списка до последнего неотсортированного элемента
#       Если текущий элемент меньше следующего:
#           Поменять их местами
#           Флаг отсутствия обменов равен False
#
#       Вывести список
#
#       Если флаг отсутствия обменов:
#           Прервать сортировку
# Вывести итоговый список
#
# Если элементов не меньше трёх:
#   Вывести первые три элемента списка

def bubble_sort_desc(a):
    n = len(a)
    comparisons = 0
    swaps = 0
    for i in range(n - 1):
        swapped = False
        for j in range(n - 1 - i):
            comparisons += 1
            if a[j] < a[j + 1]:
                a[j], a[j + 1] = a[j + 1], a[j]
                swaps += 1
                swapped = True
            print(a)
        if not swapped:
            break
    return a

n = int(input())
scores = [int(input()) for _ in range(n)]
bubble_sort_desc(scores)
print(scores, 'конечный список')

